      ,[EMP_BARCODENO]
      ,[EMP_PAYROLLNO]
      ,[EMP_ACTIVE_HRM_FLG]
      ,[EMP_ACTIVE_PAYROLL_FLG]
      ,[EMP_ACTIVE_ATT_FLG]
      ,[EMP_CONFIRM_FLG]
      ,CONVERT(nvarchar,[EMP_CONFIRM_DATE],103)
      ,CONVERT(nvarchar,[EMP_DATE_FOR_CONFIRM],103)
      ,CONVERT(nvarchar,[EMP_RESIGN_DATE],103)
      ,CONVERT(nvarchar,[EMP_RETIRE_DATE],103)
      ,[EMP_WORKHOURS]
      ,[LOCATION]
      ,[SUB_LOCATION]
      ,CONVERT(nvarchar,[EMP_CONTRACT_START_DATE],103)
      ,CONVERT(nvarchar,[EMP_CONTRACT_END_DATE],103)
      ,CONVERT(nvarchar,[EMP_CONT_TO_PERM_DATE],103)
      ,[EMP_CATEGORY]
      ,[RECR_COMPANY]
      ,[EMP_GROUP]
      ,[HIE_LEVEL_1]
      ,[HIE_LEVEL_2]
      ,[HIE_LEVEL_3]
	  ,[HIE_LEVEL_4]
      ,[HIE_LEVEL_5]
      ,[HIE_LEVEL_6]
	  ,[HIE_LEVEL_7]
      ,[HIE_LEVEL_8]
      ,[HIE_LEVEL_9]
      ,[EMP_TAXONTAX_FLG]
      ,[EMP_TAX_ID_NUMBER]
      ,[EMP_EPF_ELIGIBLE_FLG]
      ,[EMP_EPF_EMPLOYEE_AMOUNT]
      ,[EMP_EPF_EMPLOYER_AMOUNT]
      ,[EMP_ETF_ELIGIBLE_FLG]
      ,[EMP_ETF_NUMBER]
      ,[EMP_ETF_EMPLOYEE_AMOUNT]
      ,CONVERT(nvarchar,[EMP_ETF_DATE],103)
      ,[EMP_MSPS_ELIGIBLE_FLG]
      ,[EMP_MSPS_EMPLOYER_AMOUNT]
      ,[EMP_MSPS_EMPLOYEE_AMOUNT]
      ,[EMP_PER_ADDRESS1]
      ,[EMP_PER_ADDRESS2]
      ,[EMP_PER_ADDRESS3]
      ,[EMP_PER_CITY]
      ,[EMP_PER_TELEPHONE]
      ,[EMP_PER_MOBILE]
      ,[EMP_PER_FAX]
      ,[EMP_PER_EMAIL]
      ,[EMP_PER_COUNTRY]
      ,[EMP_PER_PROVINCE]
      ,[EMP_PER_DISTRICT]
      ,[EMP_PER_ELECTORATE_CODE]
      ,[EMP_TEM_ADDRESS1]
      ,[EMP_TEM_ADDRESS2]
      ,[EMP_TEM_ADDRESS3]
      ,[EMP_TEM_CITY]
