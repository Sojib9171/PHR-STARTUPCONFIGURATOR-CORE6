    END

	ELSE IF @TABLE_NAME = 'HS_HR_IA_EC_UPLOAD'
    BEGIN
       SELECT [EMP_NUMBER]
      ,[EC_CONT_PER_FULLNAME]
      ,[EC_RELATIONSHIP]
      ,[EC_RELATIONSHIP_ORDER]
      ,[EC_PER_ADDRESS]
      ,[EC_OFFICE_TELEPHONE]
      ,[EC_RES_TELEPHONE]
      ,[EC_MOBILE]
      ,[EC_CONTACT_TYPE_FLG]
      ,[EC_OFFICIAL_ADDRESS]
      ,[EC_EMP_NUMBER]
      ,[EC_TEL_EXT]
	FROM [QADBV9_L2].[HS_HR_IA_EC_UPLOAD]
		ORDER BY [ID];
    END
	
	ELSE IF @TABLE_NAME = 'HS_HR_IA_SHORT_LEAVE_UPLOAD'
    BEGIN
       SELECT 
	   [SHORT_LEAVE_TYPE_CODE]
      ,[LEAVE_TYPE_NAME]
      ,[MIN_HOURS_ALLOWED]
      ,[MAX_HOURS_ALLOWED]
      ,[MAX_OCCURRENCE]
      ,[MAX_CONSECUTIVE_HOURS_ALLOWED]
      ,[CONSIDER_ALL_LEAVE_TYPES]
      ,[MAX_NO_OF_LEAVE_PER_DAY]
      ,[LEAVE_TIME_PERIOD]
      ,[DURATION_PER_SLOT_BEG_OF_SHIFT]
      ,[DURATION_PER_SLOT_END_OF_SHIFT]
      ,[MAX_HOURS_PER_MONTH]
      ,[MAX_HOURS_PER_YEAR]
      ,[APPLICABLE_FOR_WEEKLY]
      ,[LEAVE_NEED_APPROVAL]
      ,[ENTITLEMENT_CONFIRMED_EMP]
      ,[ENTITLEMENT_UN_CONFIRMED_EMP]
      ,[COMMENTS]
	FROM [QADBV9_L2].[HS_HR_IA_SHORT_LEAVE_UPLOAD]
