      ,[EMP_TEM_FAX]
      ,[EMP_TEM_EMAIL]
      ,[EMP_TEM_COUNTRY]
      ,[EMP_TEM_PROVINCE]
      ,[EMP_TEM_DISTRICT]
      ,[EMP_TEM_ELECTORATE]
	  ,[EMP_OFFICE_EMAIL]
      ,[EMP_OFFICE_EXTN]
	  ,[EMP_OFFICE_PHONE]
	FROM [QADBV9_L2].[HS_HR_IA_EMPLOYEE_UPLOAD]
