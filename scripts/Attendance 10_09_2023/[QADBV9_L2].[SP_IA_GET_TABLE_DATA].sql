      ,[EREL_OFFICE_ADDRESS]
      ,[EREL_PF_RATIO]
      ,[EREL_PF_NOMINEE_FLG]
      ,[EREL_ENTDEATHDONATION_FLG]
      ,[EREL_LIVINGORNOT_FLG]
      ,[EREL_NIC_NUMBER]
      ,[EREL_SPOUSE_TELEPHONE]
      ,[EREL_COMMENTS]
