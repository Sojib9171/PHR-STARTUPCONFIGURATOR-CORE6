    END

	ELSE IF @TABLE_NAME = 'HS_HR_IA_STATUTRY_LEAVE_UPLOAD'
    BEGIN
	SELECT  [LEAVE_TYPE]
      ,[LEAVE_DESCRIPTION]
      ,[ACTIVE]
      ,[COVERING_EMP_REQ]
      ,[ALLOW_HALF_DAYS]
      ,[AUTO_CAL_END_DATE]
      ,[HOLIDAY_AS_LEAVE]
      ,[LIEU_LEAVE]
      ,[MATERNITY_LEAVE]
      ,[NO_PAY_LEAVE]
      ,[QUARTER_LEAVE]
      ,[SHOW_BALANCE]
      ,[EARNED_CF_BREAK_DOWN]
      ,[EXTENDED_PERIOD]
      ,[ALLOW_PLANNING]
      ,[ATTACHMENT_REQUIRED]
      ,[VIEW_ADDITIONAL_FIELD]
      ,[ALLOW_NEGATIVE]
      ,[KIOSK_ALLOWED]
      ,[HIDE_ZERO_ENTITLEMENT]
      ,[ALLOW_SELECT_APPROVER]
      ,[SHOW_LEAVE_REASON]
      ,[CONSIDER_IN_DASHBOARD]
      ,[SHOW_IN_OFFBOARDING]
      ,[CONSIDER_FOR_TIME_LOST]
      ,[COMMENT_MANDATORY]
      ,[LONG_LEAVE]
      ,[HIDE_IN_SELF_APP]
      ,[APPLY_SEQUENCE_IN_DASH]
      ,[MAXIMUM_LEA_DAYS_IN_SINGL_INS]
      ,[YEARLY_ENTITLEMENT]
      ,[MINIMUM_DAYS_IN_SNGLE_LEA_APP]
      ,[MAXIMUM_DAYS_IN_SNGLE_LEA_APP]
      ,[REQ_MEDICAL_CERTIFICATE]
      ,[APPLY_TGTHER_WITH_LEA_TYPE]
      ,[FUTURE_LEAVE_UTILIZED_AS]
      ,[UTILIZATION_PERIOD]
      ,[FUTURE_LEAVE_TYPE]
      ,[MAX_ALLOWED_FORWARD]
      ,[MAX_LIMIT]
      ,[NOTICE_PERIOD]
      ,[TIME_BASE_LEAVE]
      ,[LEAVE_APPROVAL]
      ,[LEAVE_REJECTION]
      ,[LEAVE_CANCELLATION]
      ,[ENTITLEMENT_CRITERIA]
      ,[COMMENTS]
		FROM [QADBV9_L2].[HS_HR_IA_STATUTRY_LEAVE_UPLOAD]
			ORDER BY [ID];
      END

	ELSE IF @TABLE_NAME = 'HS_HR_IA_SHIFT_INFO_UPLOAD'
	BEGIN
	SELECT  [SHIFT_NAME]
           ,[SHIFT_ABR]
           ,[START_TIME]
           ,[END_TIME]
           ,[FIRST_HALF_DUR]
           ,[SECOND_HALF_DUR]
           ,[FLEXI_SHIFT]
           ,[OFF_SHIFT]
           ,[CONTINUE_SHIFT]
           ,[LATE_COVER]
           ,[BRK_ALLOW_LATE_HRS_CALC]
           ,[ALLOW_DEDCT_OUT_HRS]
           ,[AUTO_MID_NIGHT_FIX]
           ,[NEXT_DAY_SHIFT_OUT_TIME]
           ,[LEAVE_DAYS]
           ,[OT_ROUND_METHOD]
           ,[OT_ROUNDING]
           ,[GRACE_PERIOD]
           ,[BREAK_NAME]
           ,[BREAK_START_TIME]
           ,[BREAK_DURATION]
		FROM [QADBV9_L2].[HS_HR_IA_SHIFT_INFO_UPLOAD]
			ORDER BY [ID];
      END

	  ELSE IF @TABLE_NAME = 'HS_HR_IA_ROSTER_INFO_UPLOAD'
    BEGIN
	SELECT [ROSTER_CODE]
      ,[ROSTER_NAME]
      ,[ROS_IS_ACTIVE]
      ,[ROSTER_COMMENT]
      ,[ROS_GRP_NAME]
      ,[ROS_GRP_DESC]
      ,[ROS_HIE]
      ,[ROS_COMP_CALENDER]
      ,[ROS_WRKFLW_TYPE]
      ,[DYN_ROS_SUPERVISOR]
      ,[ROS_GRP_CMNTS]
		FROM [QADBV9_L2].[HS_HR_IA_ROSTER_INFO_UPLOAD]
			ORDER BY [ID];
      END
