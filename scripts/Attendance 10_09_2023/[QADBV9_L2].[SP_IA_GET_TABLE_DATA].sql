		FROM [QADBV9_L2].[HS_HR_IA_RH_UPLOAD]
			ORDER BY [ID];
    END

    ELSE IF @TABLE_NAME = 'HS_HR_IA_DEPINFO_UPLOAD'
    BEGIN
        SELECT [EMP_NUMBER]
      ,[EREL_RELATIONFULLNAME]
      ,[EREL_RELATIONSHIP]
